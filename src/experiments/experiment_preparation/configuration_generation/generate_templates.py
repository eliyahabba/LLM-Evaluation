def get_configurations_slice(all_configs: list[str], topic_index: int) -> list[str]:
    """
    Get a slice of configurations for a specific topic.
    When reaching the end of configurations, continue from the beginning in a serial manner.
    """
    total_configs = len(all_configs)
    start_position = (topic_index * NUM_CONFIGS_PER_SUBJECT) % total_configs

    # If we need to wrap around
    if start_position + NUM_CONFIGS_PER_SUBJECT > total_configs:
        first_part = all_configs[start_position:total_configs]
        remaining_length = NUM_CONFIGS_PER_SUBJECT - (total_configs - start_position)
        second_part = all_configs[:remaining_length]
        return first_part + second_part

    # If we don't need to wrap around
    return all_configs[start_position:start_position + NUM_CONFIGS_PER_SUBJECT]

