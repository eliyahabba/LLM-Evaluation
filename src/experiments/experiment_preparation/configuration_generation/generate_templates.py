def get_all_possible_configurations(dataset_catalog_name: str, prompt_paraphrases: list[str],
                                    template_names: list[str]) -> list[str]:
    """Generate all possible template configurations."""
    import random

    templates = [
        f"{dataset_catalog_name}.{prompt}.{template}"
        for prompt in prompt_paraphrases
        for template in template_names
    ]

    # Create a shuffled copy using fixed seed for reproducibility
    templates_copy = templates.copy()
    random.Random(SAMPLING_SEED).shuffle(templates_copy)

    return templates_copy



